// Pour se proteger de la multi-inclusion : ADE_NON_COPYABLE_H doit être unique
// Le prefixe ADE_ et le suffixe _H sont obligatoires
#ifndef ADE_NON_COPYABLE_H
#define ADE_NON_COPYABLE_H


////////////////////////////////////////////////////////////////////////////////
//                                                          Forward declarations
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
//                    Defines & Types de portee fichier ou globale (hors classe)
////////////////////////////////////////////////////////////////////////////////

// On rend le constructeur de recopie et l'operateur de copie prives
#define NON_COPYABLE(MaClass)           \
private :                               \
MaClass(const MaClass&);                \
MaClass& operator=(const MaClass&);


#endif // ADE_NON_COPYABLE_H

